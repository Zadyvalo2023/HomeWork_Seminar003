// Задача 19
// Напишите программу, которая принимает на вход пятизначное число и проверяет, является ли оно палиндромом.
// 14212 -> нет
// 12821 -> да
// 23432 -> да
/*
Палиндромное число - это число, которое можно читать одинаково как слева направо,
 так и справа налево. Например, 121, 1221, 12321 и так далее являются палиндромными числами. 
Обратите внимание, что в числе палиндрома цифры расположены в том же порядке как и в обратном порядке. */

using System;

namespace Palindrome
{
    class Program
    {
        static void Main(string[] args)
        {
            Console.Write("Введите пятизначное число: ");// Ввод числа 
            int number = int.Parse(Console.ReadLine()); // Обьявляем переменную int=nember  и парсим ее 

            int first = number / 10000; // Проверка введеного числа 
            int second = (number / 1000) % 10;
            int fourth = (number / 10) % 10;
            int fifth = number % 10;

            if (first == fifth && second == fourth)//  Чтобы проверить, является ли число палиндромом, программа сравнивает первую и последнюю цифры, а также вторую и четвертую цифры. Если они равны, то выводится сообщение "Это число является палиндромом." Если они не равны, то выводится сообщение "Это число не является палиндромом."
            {
                Console.WriteLine("Это число является палиндромом.");
            }
            else
            {
                Console.WriteLine("Это число не является палиндромом.");
            }

            Console.ReadKey();
        }
    }
}

